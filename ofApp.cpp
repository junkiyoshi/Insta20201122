#include "ofApp.h"

//--------------------------------------------------------------
void ofApp::setup() {

	ofSetFrameRate(60);
	ofSetWindowTitle("openframeworks");

	ofBackground(239);
	ofSetLineWidth(2);
	ofEnableDepthTest();

	this->frame.setMode(ofPrimitiveMode::OF_PRIMITIVE_LINES);
}

//--------------------------------------------------------------
void ofApp::update() {

	ofSeedRandom(39);

	this->face.clear();
	this->frame.clear();

	int span = 15;
	for (int i = 0; i < 5; i++) {

		auto noise_seed = glm::vec2(ofRandom(1000), ofRandom(1000));
		for (int z = -300; z <= 300; z += span) {

			auto noise_location = glm::vec2(
				ofMap(ofNoise(noise_seed.x * 0.005, z * 0.001 + ofGetFrameNum() * 0.01), 0, 1, -300, 300),
				ofMap(ofNoise(noise_seed.y * 0.005, z * 0.001 + ofGetFrameNum() * 0.01), 0, 1, -300, 300));

			for (int x = -300; x <= 300; x += span) {

				for (int y = -300; y <= 300; y += span) {

					auto distance = glm::distance(glm::vec3(noise_location, z), glm::vec3(x, y, z));
					if (distance < 35) {

						this->setBoxToMesh(this->face, this->frame, glm::vec3(x, y, z), span, span, span);
					}
				}
			}
		}
	}
}

//--------------------------------------------------------------
void ofApp::draw() {

	this->cam.begin();
	ofRotateY(ofGetFrameNum() * -0.5);

	ofSetColor(239);
	this->face.drawFaces();

	ofSetColor(39);
	this->frame.drawWireframe();

	ofNoFill();
	ofDrawBox(600);

	this->cam.end();
}

//--------------------------------------------------------------
